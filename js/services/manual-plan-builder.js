(function(root){
  'use strict';

  const TYPES=['Easy','Recovery','Tempo','Interval','Long','Race','Rest'];

  function isIsoDate(value){
    if(!/^\d{4}-\d{2}-\d{2}$/.test(String(value||'')))return false;
    const parsed=new Date(`${value}T12:00:00`);
    return !Number.isNaN(parsed.getTime())&&parsed.toISOString().slice(0,10)===value;
  }
  function addDays(date,days){
    const value=new Date(`${date}T12:00:00`);
    value.setDate(value.getDate()+days);
    return value.toISOString().slice(0,10);
  }
  function round(value){return +Number(value).toFixed(2);}
  function normalizeSession(input,index){
    const date=String(input?.date||'').trim();
    const type=String(input?.type||'').trim();
    const targetDist=round(Number(input?.targetDist));
    if(!isIsoDate(date))throw new Error(`Session ${index+1}: choose a valid date.`);
    if(!TYPES.includes(type))throw new Error(`Session ${index+1}: choose a workout type.`);
    if(type==='Rest'&&targetDist!==0)throw new Error(`Session ${index+1}: Rest must have 0 km.`);
    if(type!=='Rest'&&(!Number.isFinite(targetDist)||targetDist<=0))throw new Error(`Session ${index+1}: distance must be greater than 0.`);
    const title=String(input?.title||'').trim()||`${type} run`;
    const mainSet=String(input?.mainSet||'').trim()||title;
    return {date,type,targetDist,title,mainSet,warmup:String(input?.warmup||'').trim(),cooldown:String(input?.cooldown||'').trim(),notes:String(input?.notes||'').trim()};
  }
  function createPlan(inputs,{goal='Manual running schedule',createdAt=Date.now()}={}){
    if(!Array.isArray(inputs)||!inputs.length)throw new Error('Add at least one session before saving.');
    const normalized=inputs.map(normalizeSession).sort((a,b)=>a.date.localeCompare(b.date));
    const dates=new Set();
    normalized.forEach(session=>{if(dates.has(session.date))throw new Error(`Only one session is allowed on ${session.date}.`);dates.add(session.date);});
    const startDate=normalized[0].date,endDate=normalized[normalized.length-1].date;
    const totalWeeks=Math.max(1,Math.floor((new Date(`${endDate}T12:00:00`)-new Date(`${startDate}T12:00:00`))/(7*86400000))+1);
    const weeklyTargets=Array.from({length:totalWeeks},()=>0);
    const sessions=normalized.map((session,index)=>{
      const week=Math.floor((new Date(`${session.date}T12:00:00`)-new Date(`${startDate}T12:00:00`))/(7*86400000))+1;
      weeklyTargets[week-1]=round(weeklyTargets[week-1]+session.targetDist);
      const quality=['Tempo','Interval','Race'].includes(session.type)?session.targetDist:0;
      return {
        sessionId:`manual-${createdAt}-s${index+1}`,date:session.date,week,phase:'Manual',phaseLabel:'Manual schedule',
        type:session.type,intent:'manual',targetDist:session.targetDist,targetPace:'',targetPaceRange:'',targetHR:'',
        priority:['Tempo','Interval','Long','Race'].includes(session.type)?'key':'normal',description:session.title,
        details:{warmup:session.warmup,mainSet:session.mainSet,cooldown:session.cooldown,execution:'Follow the session you entered. Change it if pain affects your form.',successCriteria:'Complete the planned session with controlled form.',intensity:'Manual',targetDescription:session.title},
        workoutSpec:{intent:'manual',structure:'manual',totalDistanceKm:session.targetDist,qualityDistanceKm:quality,sourceProvider:'manual'},
        notes:session.notes,methodologyVersion:'manual-schedule-2026.07.22'
      };
    });
    return {
      planId:`manual-${createdAt}`,revisionId:'r1',engineVersion:2,methodologyVersion:'manual-schedule-2026.07.22',status:'active',goal:String(goal||'Manual running schedule').trim()||'Manual running schedule',startDate,endDate,totalWeeks,daysPerWeek:Math.max(...weeklyTargets.map((_,week)=>sessions.filter(session=>session.week===week+1&&session.type!=='Rest').length)),createdAt,updatedAt:createdAt,
      sourcePlan:{provider:'manual',label:'Manual schedule',importedAt:createdAt,adaptation:'Sessions were entered directly by the athlete.'},
      goalProfile:{distance:'Manual',targetTime:'',targetMinutes:null,targetPace:null,benchmark:'',unavailableRaw:'',unavailable:[],longRunDay:'',longRunDayName:'',raceGoal:sessions.some(session=>session.type==='Race')},
      athleteProfile:{level:'manual',currentWeeklyKm:Math.max(...weeklyTargets),volumeBasis:'manual_schedule',paceBasis:'manual',confidence:'manual'},
      inputAudit:{source:'manual schedule'},phaseSchedule:weeklyTargets.map((targetVolumeKm,index)=>({week:index+1,phase:'Manual',phaseLabel:'Manual schedule',startDate:addDays(startDate,index*7),targetVolumeKm})),sessions,recoveryCards:[],recoverySummary:{},completedDates:{},adjustments:[],dailyDecisions:{},
      validation:{valid:true,errors:[],warnings:['manual_schedule: sessions are athlete-entered and are not generated by MyDash.'],weeklyTargetsKm:weeklyTargets,weeklyActualKm:weeklyTargets},compatibility:{legacyMirror:true,previousEngineAvailable:true,manualPlan:true}
    };
  }

  root.MyDashManualPlan={TYPES,createPlan};
})(window);
